// driver 兜底（无品牌识别时）
const DRIVER_FALLBACK: Record<string, { label: string; color: string }> = {
  local: { label: "本机存储", color: "#71717A" },
  ftp: { label: "FTP", color: "#71717A" },
  s3: { label: "S3 兼容", color: "#71717A" },
  oss: { label: "OSS", color: "#FF6A00" },
  cos: { label: "COS", color: "#00A4FF" },
  obs: { label: "OBS", color: "#CF0A2C" },
  bos: { label: "BOS", color: "#2932E1" },
};

export interface BrandInfo {
  label: string;
  color: string;
  isBrand: boolean;
}

export function getBrandInfo(provider: string, driver: string): BrandInfo {
  const brand = BRANDS[provider];
  if (brand) return { label: brand.label, color: brand.color, isBrand: true };
  const fb = DRIVER_FALLBACK[driver] ?? { label: driver.toUpperCase(), color: "#71717A" };
  return { ...fb, isBrand: false };
}

interface BrandIconProps {
  provider: string;
  driver: string;
  className?: string;
}

// 渲染品牌 SVG（带颜色），未匹配时回退到 lucide 图标
export function BrandIcon({ provider, driver, className }: BrandIconProps) {
  const brand = BRANDS[provider];
  if (brand?.svg) {
    return (
      <svg
        className={className}
        viewBox="0 0 24 24"
        xmlns="http://www.w3.org/2000/svg"
        fill={brand.color}
        aria-hidden="true"
      >
        <path d={brand.svg} />
      </svg>
    );
  }
  if (driver === "local") return <HardDrive className={className} />;
  if (driver === "ftp") return <Server className={className} />;
  return <Cloud className={className} />;
}
